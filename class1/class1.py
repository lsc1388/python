print("hello from prji.py")  # 這是把字串印出來的指令

# 基本型態 basic types
print(1)  # 整數int
print(1.1)  # 福點數float,只要有小數點就是浮點數
print("hello")  # 字串str,  用雙引號:""或單引號''包起來的都是字串
print(True)  # 布林值bool1,第一個字母要大寫
print(None)  # 空值None

# 變數variable
a = 1  # 把這個整數存到電腦的記憶體,並且取名為A
# =的功能是將右邊的資料存到左邊的變數空間當中
print(a)  # 把a的店數的值印出來
